// Instala e salva no cache
self.addEventListener("install", event => {
  event.waitUntil(
    caches.open(CACHE_NAME).then(cache => {
      console.log("[Service Worker] Cacheando arquivos de áudio...");
      return cache.addAll(FILES_TO_CACHE);
    })
  );
});

// Busca primeiro no cache, depois na rede
self.addEventListener("fetch", event => {
  event.respondWith(
    caches.match(event.request).then(cachedResponse => {
      if (cachedResponse) {
        return cachedResponse; // Se está no cache, usa ele
      }
      return fetch(event.request); // Se não, baixa da rede
    })
  );
});
